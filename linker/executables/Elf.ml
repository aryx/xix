(* ------------------------------------------------------------------------- *)
(* Exec header *)
(* ------------------------------------------------------------------------- *)

type exec_header = {
  ident: unit;
}

and ident_class =
  | CNone
  | C32
  | C64
  | CNum

and ident_data = 
  | DNone
  | DLSB
  | DMSB
  | DNum

and machine =
  | MNone
  | MM32
  | MSparc
  | MI386
  | MM68K
  | MM88K
  | MI486
  | MI860
  | MMips
  | MS370
  | MMipsr4K
  | MSparc64
  | MPower
  | MPower64
  | MArm
  | MAmd64
  | MArm64
  | MRiscv

